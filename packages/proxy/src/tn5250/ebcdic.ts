// Build reverse lookup: Unicode code point → EBCDIC byte (CP37)
const UNICODE_TO_EBCDIC = new Map<number, number>();
for (let i = 0; i < 256; i++) {
  UNICODE_TO_EBCDIC.set(EBCDIC_TO_UNICODE[i], i);
}

// Reverse lookup for CP290 — built lazily on first use.
let UNICODE_TO_CP290: Map<number, number> | null = null;
function getUnicodeToCp290(): Map<number, number> {
  if (!UNICODE_TO_CP290) {
    UNICODE_TO_CP290 = new Map();
    for (let i = 0; i < 256; i++) {
      const cp = EBCDIC_CP290_TO_UNICODE[i];
      if (cp > 0) UNICODE_TO_CP290.set(cp, i);
    }
  }
  return UNICODE_TO_CP290;
}

/**
 * Convert an EBCDIC byte to a UTF-8 character using the specified code page.
 * Defaults to CCSID 37 for backward compatibility.
 */
export function ebcdicToChar(byte: number, codePage: EbcdicCodePage = 'cp37'): string {
  const table = codePage === 'cp290' ? EBCDIC_CP290_TO_UNICODE : EBCDIC_TO_UNICODE;
  const cp = table[byte & 0xFF];
  return cp > 0 ? String.fromCharCode(cp) : ' ';
}

/**
 * Convert an EBCDIC byte to a UTF-8 character using the IBM 5250
 * APL/Symbol character set (CGCS). Maps common indicator and
 * line-drawing positions to Unicode equivalents; falls back to
 * the default CCSID 37 mapping for unmapped positions.
 */
const SYMBOL_MAP: Record<number, string> = {
  0x42: '↓', // scroll-down indicator (â in CCSID 37)
  0x44: '↑', // scroll-up indicator (à in CCSID 37)
  0xBF: '─', // horizontal line
  0x6A: '│', // vertical line
  0xAB: '┌', // top-left corner
  0xAD: '└', // bottom-left corner
  0xBB: '┐', // top-right corner
  0xBD: '┘', // bottom-right corner
  0xAC: '├', // left tee
  0xBC: '┤', // right tee
  0xBE: '┴', // bottom tee
  0xAE: '┬', // top tee
  0x8C: '┼', // cross
};

export function ebcdicSymbolChar(byte: number): string {
  return SYMBOL_MAP[byte] ?? ebcdicToChar(byte);
}

/** Convert a UTF-8 character to an EBCDIC byte (specified code page). */
export function charToEbcdic(char: string, codePage: EbcdicCodePage = 'cp37'): number {
  const cp = char.charCodeAt(0);
  const table = codePage === 'cp290' ? getUnicodeToCp290() : UNICODE_TO_EBCDIC;
  const eb = table.get(cp);
  return eb !== undefined ? eb : 0x40; // default to EBCDIC space
}

/** Convert an EBCDIC buffer to a UTF-8 string */
export function ebcdicBufferToString(buf: Buffer, start: number = 0, length?: number): string {
  const end = length !== undefined ? start + length : buf.length;
  let result = '';
  for (let i = start; i < end; i++) {
    result += ebcdicToChar(buf[i]);
  }
  return result;
}

/** Convert a UTF-8 string to an EBCDIC buffer */
export function stringToEbcdicBuffer(str: string): Buffer {
  const buf = Buffer.alloc(str.length);
  for (let i = 0; i < str.length; i++) {
    buf[i] = charToEbcdic(str[i]);
  }
  return buf;
}

// EBCDIC space = 0x40
export const EBCDIC_SPACE = 0x40;
// EBCDIC null = 0x00
export const EBCDIC_NULL = 0x00;
